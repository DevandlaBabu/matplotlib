import matplotlib.pyplot as plt
import numpy as np

scores=np.random.normal(loc=70,scale=10,size=100)
scores=np.clip(scores,0,100)

plt.hist(scores,bins=10,
            color='lightblue',
            edgecolor='black')

plt.title('Exam Score')
plt.xlabel('scores')
plt.ylabel('# of students')

plt.show()